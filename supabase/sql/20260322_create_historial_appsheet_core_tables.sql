  codigo text,
  nombre text,
  info text,
  marca text,
  modelo text,
  precio_unitario numeric(12,2) default 0,
  foto_art_raw text,
  foto_art_url text,
  source_sheet text default 'ARTICULOS',
  payload jsonb,
  updated_at timestamptz not null default now()
);

create unique index if not exists ux_historial_appsheet_articulos_id_articulo
  on public.historial_appsheet_articulos (id_articulo);

create index if not exists idx_historial_appsheet_articulos_codigo
  on public.historial_appsheet_articulos (codigo);

create index if not exists idx_historial_appsheet_articulos_updated_at
  on public.historial_appsheet_articulos (updated_at desc);
